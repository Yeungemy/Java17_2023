public class Decimals {
    
    public static void main(String[] args) {
        double percentage = 7.0;

        System.out.println(percentage);

        int wallet = 20; 
        int people = 3;

        // result for same type of numbers is keep the same
        System.out.println(wallet / people);

        // alway use double for math calculation
        double wallet_1 = 20; 

        System.out.println(wallet_1 / people);
    }
}
